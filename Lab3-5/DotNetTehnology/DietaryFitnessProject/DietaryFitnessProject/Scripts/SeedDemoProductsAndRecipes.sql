INSERT INTO [dbo].[Recipe] (
    [RecipeId],
    [RecipeName],
    [RecipeInstructions],
    [PortionCount],
    [RecipeTotalGrams],
    [RecipeCalories],
    [RecipeProteins],
    [RecipeFats],
    [RecipeCarbs]
)
SELECT
    r.[RecipeId],
    r.[RecipeName],
    r.[RecipeInstructions],
    r.[PortionCount],
    SUM(l.[Grams]),
    SUM(l.[Grams] / 100.0 * p.[ProductCaloriesPer100g]),
    SUM(l.[Grams] / 100.0 * p.[ProductProteinsPer100g]),
    SUM(l.[Grams] / 100.0 * p.[ProductFatsPer100g]),
    SUM(l.[Grams] / 100.0 * p.[ProductCarbsPer100g])
FROM [#rec] r
INNER JOIN [#rp] l ON l.[RecipeId] = r.[RecipeId]
INNER JOIN [dbo].[Product] p ON p.[ProductId] = l.[ProductId]
GROUP BY r.[RecipeId], r.[RecipeName], r.[RecipeInstructions], r.[PortionCount];

INSERT INTO [dbo].[RecipeProduct] ([RecipeId], [ProductId], [Grams])
